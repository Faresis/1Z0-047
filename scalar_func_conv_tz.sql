/* from_tz : timestamp + tz = timestamp with tz */
SELECT FROM_TZ( TIMESTAMP '2012-10-12 07:45:30', '+07:30') FROM DUAL;
SELECT FROM_TZ( TIMESTAMP '2012-10-12 07:45:30', 'HST') FROM DUAL;

/* to_timestamp_tz */
SELECT TO_TIMESTAMP_TZ('17-04-2013 16:45:30','DD-MM-RRRR HH24:MI:SS') "Time" FROM DUAL;
SELECT TO_TIMESTAMP_TZ('17-04-2013 16:45:30 EST','DD-MM-RRRR HH24:MI:SS TZR') "Time" FROM DUAL;
SELECT TO_TIMESTAMP_TZ('17-04-2013 16:45:30 +03:00','DD-MM-RRRR HH24:MI:SS TZH:TZM') "Time" FROM DUAL;

/* cast */
SELECT CAST('19-JAN-10 11:35:30' AS TIMESTAMP WITH LOCAL TIME ZONE) "Converted LTZ" FROM DUAL;
SELECT CAST('19-JAN-10' AS DATE) "Converted DATE" FROM DUAL;
SELECT CAST('19-JAN-10 11:35:30 AM EST' AS TIMESTAMP WITH TIME ZONE) "Converted LTZ" FROM DUAL;

/* extract */
SELECT EXTRACT(YEAR FROM DATE '2015-02-01') "Year" FROM DUAL;
SELECT EXTRACT(MONTH FROM DATE '2015-02-01') "Month" FROM DUAL;
SELECT EXTRACT(DAY FROM DATE '2015-02-01') "Day" FROM DUAL;
SELECT EXTRACT(HOUR FROM TO_TIMESTAMP('2009-10-11 12:13:14', 'RRRR-MM-DD HH24:MI:SS')) "Hour" FROM DUAL;
SELECT EXTRACT(MINUTE FROM TO_TIMESTAMP('2009-10-11 12:13:14', 'RRRR-MM-DD HH24:MI:SS')) "Minute" FROM DUAL;
SELECT EXTRACT(SECOND FROM TO_TIMESTAMP('2009-10-11 12:13:14', 'RRRR-MM-DD HH24:MI:SS')) "Second" FROM DUAL;
SELECT EXTRACT(TIMEZONE_HOUR FROM TO_TIMESTAMP_TZ('2009-10-11 12:13:14 EST', 'RRRR-MM-DD HH24:MI:SS TZR')) "Time zone hour" FROM DUAL;
SELECT EXTRACT(TIMEZONE_MINUTE FROM TO_TIMESTAMP_TZ('2009-10-11 12:13:14 EST', 'RRRR-MM-DD HH24:MI:SS TZR')) "Time zone minute" FROM DUAL;
SELECT EXTRACT(TIMEZONE_REGION FROM TO_TIMESTAMP_TZ('2009-10-11 12:13:14 EST', 'RRRR-MM-DD HH24:MI:SS TZR')) "Time zone region" FROM DUAL;
SELECT EXTRACT(TIMEZONE_ABBR FROM TO_TIMESTAMP_TZ('2009-10-11 12:13:14 EST', 'RRRR-MM-DD HH24:MI:SS TZR')) "Time zone abbr" FROM DUAL;
select extract(TIMEZONE_HOUR from TO_TIMESTAMP_TZ('2009-10-11 12:13:14 America/Los_Angeles', 'RRRR-MM-DD HH24:MI:SS TZR')) "Time zone hour" FROM DUAL;

/* sys_extract_utc : extracts utc datetime value */
select sys_extract_utc(current_timestamp), sys_extract_utc(systimestamp) from dual;
SELECT SYS_EXTRACT_UTC(TIMESTAMP '2012-03-25 09:55:00 -04:00') "HQ" FROM DUAL;

/* AT TIME ZONE */
SELECT DBTIMEZONE, SESSIONTIMEZONE FROM DUAL;
SELECT TO_TIMESTAMP('2012-MAY-24 02:00:00','RRRR-MON-DD HH24:MI:SS') AT TIME ZONE DBTIMEZONE "DB Time" FROM DUAL;
SELECT TO_TIMESTAMP('2012-MAY-24 02:00:00','RRRR-MON-DD HH24:MI:SS') AT TIME ZONE 'America/Chicago' "DB Time" FROM DUAL;
SELECT TO_TIMESTAMP('2012-MAY-24 02:00:00','RRRR-MON-DD HH24:MI:SS') AT TIME ZONE '+04:00' "DB Time" FROM DUAL;
SELECT TO_TIMESTAMP_TZ('2012-MAY-24 02:00:00 +03:00','RRRR-MON-DD HH24:MI:SS TZH:TZM') AT TIME ZONE '+04:00' "DB Time" FROM DUAL;
SELECT TO_TIMESTAMP_TZ('2012-MAY-24 02:00:00 +03:00','RRRR-MON-DD HH24:MI:SS TZH:TZM') AT TIME ZONE DBTIMEZONE "DB Time" FROM DUAL;
SELECT TO_TIMESTAMP_TZ('2012-MAY-24 02:00:00 +03:00','RRRR-MON-DD HH24:MI:SS TZH:TZM') AT TIME ZONE '+01:00' "DB Time" FROM DUAL;

/* AT LOCAL */
