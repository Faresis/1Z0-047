/* from_tz : timestamp + tz = timestamp with tz */
SELECT FROM_TZ( TIMESTAMP '2012-10-12 07:45:30', '+07:30') FROM DUAL;
SELECT FROM_TZ( TIMESTAMP '2012-10-12 07:45:30', 'HST') FROM DUAL;

/* to_timestamp_tz */
SELECT TO_TIMESTAMP_TZ('17-04-2013 16:45:30','DD-MM-RRRR HH24:MI:SS') "Time" FROM DUAL;
SELECT TO_TIMESTAMP_TZ('17-04-2013 16:45:30 EST','DD-MM-RRRR HH24:MI:SS TZR') "Time" FROM DUAL;
SELECT TO_TIMESTAMP_TZ('17-04-2013 16:45:30 +03:00','DD-MM-RRRR HH24:MI:SS TZH:TZM') "Time" FROM DUAL;

/* cast */
SELECT CAST('19-JAN-10 11:35:30' AS TIMESTAMP WITH LOCAL TIME ZONE) "Converted LTZ" FROM DUAL;
SELECT CAST('19-JAN-10' AS DATE) "Converted DATE" FROM DUAL;
SELECT CAST('19-JAN-10 11:35:30 AM EST' AS TIMESTAMP WITH TIME ZONE) "Converted LTZ" FROM DUAL;